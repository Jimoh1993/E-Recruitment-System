						<h3>Establishment</h3>
						<p style="text-align: justify; text-justify: inter-word;">Dr. Bode Ayorinde (FCIB, LL.B, BL, Ph.D), currently a member of the House of Representatives, joined the then Cooperative Bank Plc in 1983 with a National Diploma in Secretarial Studies. Desirous of making progress in the Banking Industry, he registered for the Chartered Institute of Bankers of Nigeria (CIBN) professional examinations and attended the Institute’s Tuition Centre at Ibadan to pass the first eight (8) subjects in Part 1 of the institute’s examination. However, there was no tuition Centre in Ibadan to teach the remaining eight (8) subjects in Part 2 of the Institute’s examination, all Ibadan students had to go to Lagos every weekend for lectures. This vacuum in Ibadan propelled Dr. Bode Ayorinde to establish the Achievers Continuing Education Centre in 1991 at Ibadan. The Centre provided tuition for the professional examinations of CIBN, ATS and ICAN students. The Centre later added GCE and JAMB coaching, anchored the satellite campus of Ondo State Polytechnic at Ibadan, founded the Achievers High School, Ibadan and served as the spring board upon which Achievers University was established in Owo, his home town, in 2008.</p>
						<h3>Ownership</h3>
						<p style="text-align: justify; text-justify: inter-word;">Achievers Continuous Education Centre was initially a family business of Hon. Dr. Bode Ayorinde. The business was formally registered in 1994 as a private limited liability company. However, the decision to expand the scope of the Educational Institute towards the establishment of a private University caused the family to invite notable personalities to join the company. The offer, through a private placement attracted important personalities like Chief Mrs. Olutoyin Olakunri, Sen. T.O Olupitan, Late Sen Kolawole, Professors and Senior Academics to subscribe to the shares. In all, 90 shareholders emerged and became the owners of the University.</p>
						 
